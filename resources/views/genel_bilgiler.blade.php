    <h4 class="mt-6 mb-6">Ölçme ve Değerlendirme</h4>
    <p>Her dönem en az bir vize sınavı uygulanır. Vizelerin yanı sıra, her dönemin başında verilen dönem içeriğinde tarihleri belirtilen proje ve ödevler verilir. Her dönemin sonunda, öğrencinin final sınavına girmek zorundadır. Kriterler (ara sınav, proje, ödev ve final gibi) ve dönem sonu notuna etki oranları dönem başında dağıtılan ve/veya web sitesinde yayınlanan ders içerikleri açık biçimde belirtilir. Öğrenci yönetmeliği ve akademik takvime göre final sınavları üniversite tarafından belirlenen ve ilan edilen tarih ve yer ve zamanlarda yapılır.  Öğrencinin dönem sonu notu ders sorumlusu tarafından ara sınav, proje, ödev ve final sınavı sonuçlarına göre verilir.</p>
    <p>Namık Kemal Üniversitesinde geçme notu 100 üzerinden 60’tır. Ancak Final ya da bütünleme sınav sonucunun en az 50 olması zorunludur. Sınavlar, tam not 100 üzerinden değerlendirilir. Bir dersin yarıyıl/yıl sonu notu; ara sınavın veya ara sınavların aritmetik ortalamasının %30’u ile yarıyıl/yıl sonu sınavında veya bütünleme sınavında alınan notun %70’inin toplamıdır. Ancak, bir dersin yarıyıl/yıl sonu notu; ilgili fakülte/yüksekokul kurulunca karar alınmak ve yarıyıl başında ilan edilmek şartıyla ara sınavın veya ara sınavların aritmetik ortalamasının %30’u ile %50’si arasında, yarıyıl/yıl sonu veya bütünleme sınavında alınan notun %70’i ile %50’si arasında değişebilen ve oranlar toplamı %100 olacak şekilde de hesaplanabilir. Hesaplama sonucunda, ondalık noktadan sonra gelen ilk sayı beşten küçük ise bir alt tam sayıya, beş veya beşten büyük ise bir üst tam sayıya yükseltilerek kesinleştirilir.</p>
